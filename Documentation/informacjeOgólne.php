BioApp to aplikacja wspierająca samodzielne monitorowanie parametrów biomedycznych. Pozwala na zapisywanie, usuwanie oraz
przeglądanie wykonanych pomiarów. Wśród dostępnych parametrów można wymienić: tętno, ciśnienie, saturację i poziom cukru.
W zakładce "Konto" dostępne są funkcje związane z zarządzaniem kontem - wyświetlanie i edycja danych, usunięcie konta oraz
wylogowanie.
